enum WsConnectionState {
  disconnected, // Соединение закрыто намеренно
  connecting, // Пытаемся установиться прямо сейчас
  connected, // Все ок, обмениваемся данными
  reconnecting, // Связь оборвалась, восстанавливаемся в фоне
  failed, // Сервер лежит, лимит попыток исчерпан, ждем ручного действия
}
